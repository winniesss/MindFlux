export const t = (key: string, lang: Language, params?: any): string => {
  let text = translations[lang]?.[key] || translations['en']?.[key] || key;
  if (params && typeof text === 'string') {
    Object.keys(params).forEach(p => {
      text = text.replace(`{${p}}`, params[p]);
    });
  }
  return text;
};
